// This is an example of parameterized function since there is one parameter initialized inside the function. 
function multipleOfThree(number){//header
	return number**3//body
}
x=multipleOfThree(5)//function is being called and it's value is being stored in x
console.log(x)//prints the value

/*Whenvever the function is being called it will go inside that function
and returns the result and in this case, it will result 125 (since 5^3=125).
Afterward it will store that value in x and later it will print that value. */