// This is an example of parameterized function since there is one parameter initialized inside the function. 
function myName(name){ //header
	//Concatenating two things 
	return "My name is "+name //body
}
console.log(myName("Brijesh")) // this is when funtion is being called 

/*What this program does is whenever the function is being called it will
go inside that function and returns the result and in this case, it will print
My name is Brijesh.*/